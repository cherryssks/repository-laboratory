money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

months = 0  # Количество месяцев, которые удастся протянуть

current_spend = spend  # Текущие расходы

while True:
    # Бюджет текущего месяца составляет зарплату плюс остаток от подушки безопасности
    current_budget = salary + money_capital

    if current_budget >= current_spend:
        # Если бюджет больше или равен расходам, увеличиваем счетчик месяцев
        months += 1
        # Учитываем остаток подушки безопасности после покрытия расходов
        money_capital -= (current_spend - salary) if current_spend > salary else 0
    else:
        # Если бюджет недостаточен, выходим из цикла
        break

    # Увеличение расходов на 5% для следующего месяца
    current_spend *= (1 + increase)
# TODO Посчитайте количество  месяцев, которое можно протянуть без долгов

print("Количество месяцев, которое можно протянуть без долгов:", months)
